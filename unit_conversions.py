def miles_to_km(miles):
    calc = round((miles * 1.60934), 2)
    return calc
def km_to_miles(km):
    calc = round((km / 1.60934), 2)
    return calc
def fahrenheit_to_celsius(f):
    calc = round((f - 32) * (5/9))
    return calc
def celsius_to_fahrenheit(c):
    calc = round((c * (9/5)) + 32)
    return calc
def kilograms_to_pounds(kg):
    calc = round((kg * 2.20462262185), 2)
    return calc
def pounds_to_kilograms(lb):
    calc = round((lb * 0.45359237), 2)
    return calc

